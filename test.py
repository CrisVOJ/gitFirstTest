def suma(a, b):
    print(f'Suma: {a} + {b} = {a+b}')

def resta(a, b):
    print(f'Resta: {a} - {b} = {a-b}')

def multiplicacion(a, b):
    print(f'Multiplicacion: {a} x {b} = {a*b}')

def division(a, b):
    print(f'Division: {a} / {b} = {a/b}')

suma(1, 2)
resta(1, 2)
multiplicacion(1, 2)
division(1, 2)